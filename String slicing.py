# Get the characters from position 2 to position 5 (not included)
DNA1 = "ATACGATACTGA"
print(DNA1[2:5])

# Slice From the Start
RNA1 = "CAGAUAGACGAUAGACGAUA"
print(RNA1[:5])

# Slice To the End
DNA2 = "ATATGATAGACATAGGAGA"
print(DNA2[2:])

# Negative Indexing
RNA2 = "ACAGAUAGAUAGACAG"
print(RNA2[-5:-2])
